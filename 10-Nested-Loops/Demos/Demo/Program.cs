//Clock
//за всеки един час от 0 до 23
for (int hour = 0; hour <= 23; hour++)
{
    //всяка една минута 0 до 59
    for (int minute = 0; minute <= 59; minute++)
    {
        Console.WriteLine($"{hour}:{minute:D2}");
    }
}

