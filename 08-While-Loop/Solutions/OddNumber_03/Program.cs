//повтарящо се действие неопределен брой пъти -> while цикъл

//повтаряме: въвеждаме стойност на число
//спираме: въведената стойност е нечетно число
//продължаваме: въведената стойност е четно число

int number = int.Parse(Console.ReadLine()); //стойността на въведеното число

while (number % 2 == 0)
{
    number = int.Parse(Console.ReadLine());
}

//имаме въведено нечетно число в променливата number
Console.WriteLine(number);

