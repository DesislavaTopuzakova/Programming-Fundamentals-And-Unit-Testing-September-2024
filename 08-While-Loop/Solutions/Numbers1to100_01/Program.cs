//While цикъл: нямате точен брой повторения и зависим от някакво условие
//повтаряме: отпечатваме число и го увеличавам с 1
//стоп: числото > 100
//продължаваме: числото <= 100

int number = 1;
while (number <= 100)
{
    Console.WriteLine(number);
    number++;
}

//For цикъл: имате точен брой повторения
//1. начало -> 1
//2. край -> 100
//3. повтаряме -> отпечатваме число
//4. промяна -> увеличавам с 1

for (int num = 1; num <= 100; num++)
{
    Console.WriteLine(num);
}
